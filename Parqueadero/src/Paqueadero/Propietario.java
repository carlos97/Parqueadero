public class Propietario {
    private char genero;
    private int ebrio;
    private boolean gafas;

    public char getGenero() {
        return genero;
    }

    public void setGenero(char genero) {
        this.genero = genero;
    }

    public int getEbrio() {
        return ebrio;
    }

    public void setEbrio(int ebrio) {
        this.ebrio = ebrio;
    }

    public boolean isGafas() {
        return gafas;
    }

    public void setGafas(boolean gafas) {
        this.gafas = gafas;
    }

    
    
    
}
